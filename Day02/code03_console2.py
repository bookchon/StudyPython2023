# 콘솔출력 - 엔터를 공백으로 변경
print("*", end=' ') # end를 쓰지 않으면 print의 마지막은 엔터, end를 쓰면 공백 하나를 띄우고 별표가 붙음
print("**") # 모든 print의 뒤에 end를 쓰면 * ** *** **** *****로 출력 됨(한 줄에 다 나옴)
print("***") #f12누르면 개발 내용(설명) 툴 나옴
print("****")
print("*****")

# 공백 구분자를 /로 변경
print('a','b','c', sep='/') # sep -> separate (단어 별 분리하여 넣기)
print('a','b','c', sep='-')
